package entity;

public class Style {

    // 风格（如：木棉、拿铁）
    private String style;

    // 风格版本（如：4.0）
    private int version;

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

}
